#include "libft.h"

int	ft_isalpha(int i)
{
	return ((i >= 65 && i <= 90) || (i >= 97 && i <= 122));
}

int	main(void)
{
	if(ft_isalpha('%'))
		printf("Es alpha");
	else
		printf("No es alpha");
}